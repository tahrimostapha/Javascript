function surfaceRectagle(){
  var width = Number.parseInt(prompt("Veuillez saisir une largeur."));
  var length = Number.parseInt(prompt("Veuillez saisir une longueur."));
  alert("La surface calculée est de "+(width*length)+" unité.");
}

function periRectangle(){
  var width = Number.parseInt(prompt("Veuillez saisir une largeur."));
  var length = Number.parseInt(prompt("Veuillez saisir une longueur."));
  alert("Le périmètre calculé est de "+((2*width)+(2*length))+" unité.");
}
